function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
aA= get(handles.edit3,'string');
aB= get(handles.edit4,'string');
aC= get(handles.edit5,'string');
aD= get(handles.edit6,'string');

bA = str2num(aA);
bB = str2num(aB);
bC = str2num(aC);
bD = str2num(aD);

%-----------------------------------------
awal= str2num(get(handles.edit1,'string'));
akhir= str2num(get(handles.edit2,'string'));
if awal<akhir;
    
handles.x = awal:0.1:akhir;
y =handles.x.^2;

y= bA*handles.x.^bB +bC*handles.x+bD;
plot(handles.x,y);

elseif awal>=akhir;
     msgbox('x2 must be larger than x1','Peringatan','warn');
else awal==0,akhir==0;
    msgbox('DATA KOSONG','Peringatan','warn');
end

%-----------------------------------------
grid on;

judul= sprintf('Graphic Y = %dx^%d + %dx +%d',bA,bB,bC,bD);
title(judul);


